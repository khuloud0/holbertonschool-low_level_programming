#ifndef DOG_H
#define DOG_H

/**
 * struct dog - a dog's basic info
 * @name: First member (dog's name)
 * @age: Second member (dog's age)
 * @owner: Third member (dog's owner)
 *
 * Description: Define a struct to store dog information
 */
struct dog
{
	char *name;
	float age;
	char *owner;
};

#endif /* DOG_H */
